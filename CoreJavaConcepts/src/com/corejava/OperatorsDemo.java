package com.corejava;
/*
 * Arithmetic +,-,*,/,%
 * Bitwise
 * Relational
 * Logical 
 * 
 */

public class OperatorsDemo {
	
	public static void main(String[] args) {
		
		int i =16;
		int j = 8;
		
		System.out.println(i+j);
		System.out.println(i-j);
		System.out.println(i*j);
		System.out.println(i/j);
		System.out.println(i%j);
		
	}

}
